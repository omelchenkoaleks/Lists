package com.omelchenkoaleks.lists._009_text_image;

public class Day {
    private String day;
    private int temperature;

    public Day(String day, int temperature) {
        this.day = day;
        this.temperature = temperature;
    }

    public String getDay() {
        return day;
    }

    public int getTemperature() {
        return temperature;
    }
}
