package com.omelchenkoaleks.lists._006_listeners_in_list;

public class People {
    private String name;
    private int age;
    private String position;

    public People(String name, int age, String position) {
        this.name = name;
        this.age = age;
        this.position = position;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getPosition() {
        return position;
    }
}
